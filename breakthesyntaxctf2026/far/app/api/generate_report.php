<?php
session_start();

if (!isset($_SESSION['user_id'])) {
    echo json_encode(['success' => false, 'message' => 'Unauthorized']);
    exit;
}

require_once __DIR__ . '/../db.php';
require_once __DIR__ . '/../vendor/autoload.php';

try {
    $stmt = $pdo->query("
        SELECT 
            c.id,
            c.company_name,
            c.cost_per_archive_gb,
            c.registration_date,
            COALESCE(SUM(a.size), 0) as total_storage,
            COALESCE(SUM(a.size * c.cost_per_archive_gb), 0) as revenue
        FROM clients c
        LEFT JOIN archives a ON c.id = a.client_id
        GROUP BY c.id, c.company_name, c.cost_per_archive_gb, c.registration_date
        ORDER BY c.registration_date DESC
    ");
    
    $clients = $stmt->fetchAll(PDO::FETCH_ASSOC);
    
    // Fetch user information
    $userStmt = $pdo->prepare("SELECT username, profile_pic FROM users WHERE id = ?");
    $userStmt->execute([$_SESSION['user_id']]);
    $user = $userStmt->fetch(PDO::FETCH_ASSOC);
    
    // Calculate totals
    $total_clients = count($clients);
    $total_revenue = array_sum(array_column($clients, 'revenue'));
    
    // Build client rows HTML
    $clientRowsHtml = '';
    foreach ($clients as $client) {
        $clientRowsHtml .= sprintf(
            '<tr>
                <td style="padding: 10px; border-bottom: 1px solid #ddd;">%s</td>
                <td style="padding: 10px; border-bottom: 1px solid #ddd; text-align: right;">$%s</td>
                <td style="padding: 10px; border-bottom: 1px solid #ddd; text-align: center;">%s</td>
                <td style="padding: 10px; border-bottom: 1px solid #ddd; text-align: right;">%s GB</td>
                <td style="padding: 10px; border-bottom: 1px solid #ddd; text-align: right;">$%s</td>
            </tr>',
            htmlspecialchars($client['company_name']),
            number_format($client['cost_per_archive_gb'], 2),
            htmlspecialchars($client['registration_date']),
            number_format($client['total_storage'], 2),
            number_format($client['revenue'], 2)
        );
    }
    
    // Build employee section HTML
    $employeeHtml = sprintf(
        '<div style="margin-top: 40px; padding-top: 20px; border-top: 2px solid #333; text-align: center;">
            <p style="font-size: 14px; color: #666; margin-bottom: 10px;">Report generated by employee: <strong>%s</strong></p>
            %s
        </div>',
        htmlspecialchars($user['username']),
        $user['profile_pic'] ? '<img src="../uploads/' . htmlspecialchars($user['profile_pic']) . '" alt="Profile Picture" style="max-width: 100px; max-height: 100px; border-radius: 50%; margin-top: 10px;" />' : ''
    );
    
    // Load HTML template
    $htmlTemplate = file_get_contents(__DIR__ . '/report_template.html');
    
    // Replace placeholders
    $html = str_replace([
        '{{GENERATED_DATE}}',
        '{{TOTAL_CLIENTS}}',
        '{{TOTAL_REVENUE}}',
        '{{CLIENT_ROWS}}',
        '{{EMPLOYEE_SECTION}}'
    ], [
        date('F j, Y \a\t g:i A'),
        $total_clients,
        number_format($total_revenue, 2),
        $clientRowsHtml,
        $employeeHtml
    ], $htmlTemplate);
    
    // Generate PDF
    $mpdf = new \Mpdf\Mpdf(['tempDir' => __DIR__ . '/../tmp', 'debug' => true]);
    $mpdf->WriteHTML($html);
    $mpdf->Output('revenue_report_' . date('Y-m-d') . '.pdf', 'I');
    
} catch (Exception $e) {
    header('Content-Type: application/json');
    echo json_encode([
        'success' => false,
        'message' => $e->getMessage()
    ]);
}


